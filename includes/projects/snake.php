				<p class="bodyText">
					This is a group project that I participated in during my time at Chalmers as a part of a programming course. I worked on this
					project together with three other students. It is essentially a Android-version of the classic game Snake. The biggest difference
					is that in our game the user uses the motion sensor of the phone to maneuver the snake, the snake can be steered in any angle and not
					just 90 degree angles like in traditional snake games.
					<br><br>
					Another difference is that the game has different maps with different attributes.
					The speed of the snake, the pace at which it grows and obstacles are map specific. The attributes of the map is determined by an
					XML-document for each map, because of this creating new maps is fairly easy. We also developed a website where users can create
					their own maps in a Javascript editor, upload it to a database and browse other usermade maps.
					<br><br>
					Some time later I decided to continue developing the app myself. I fixed some bugs that occured since the app was not updated with
					the latest version of Android and I also added some new features like a global highscore database, improved graphics, more maps,
					sound effects and music etc.
					<br><br>
					Below you can view the report that was a part of the course as well as the map editor and two different versions of the app itself.
					The 1.0 version is the one that was developed with the three other students at Chalmers, the 1.1 version is the updated version
					that I continued to develop afterwards. The app has to be installed manually using a file browser on your phone, you also need to
					set your phone to allow apps from unknown sources.
